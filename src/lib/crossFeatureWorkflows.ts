// ── Tab A: Crew no-show → meeting action item ─────────────────────────────
//
// Fires from: the daily-log expected-vs-actual widget when the GC taps
// "Create action item" on a 60+ min late crew. Idempotent via crew_attendance
// (one row per crew_id+date, with meeting_action_item_id back-linking).
//
// We intentionally route through meetings/action_items (rather than a flat
// task) because no-show calls naturally belong on a daily standup agenda
// and the existing action-item sweep then converts them to tasks if the
// PM doesn't close them within the day.

export async function runCrewNoShowChain(input: {
  crewId: string;
  projectId: string;
  date: string; // ISO YYYY-MM-DD
}): Promise<WorkflowResult> {
  try {
    const { crewId, projectId, date } = input;

    // Idempotency: if today's attendance row already has a linked action
    // item, this chain has already fired.
    const { data: attendance } = await from('crew_attendance')
      .select('id, meeting_action_item_id, no_show_flagged_at')
      .eq('crew_id', crewId)
      .eq('attendance_date', date)
      .maybeSingle();
    if (attendance?.meeting_action_item_id) {
      return { workflow: 'crew_no_show', skipped: { reason: 'action item already created for this crew today' } };
    }

    // Pull the crew details for the action-item description.
    const { data: crew } = await from('crews')
      .select('id, name, trade, lead_id, planned_arrival_time, project_id')
      .eq('id', crewId)
      .maybeSingle();
    if (!crew) return { workflow: 'crew_no_show', error: 'crew not found' };

    // Find the most recent meeting in this project to attach the action
    // item to. If there's no meeting, fall through and use a placeholder
    // meeting_id of the crew_id (the action-item sweep will still pick it
    // up — it doesn't require a real meeting). Schema requires meeting_id
    // NOT NULL though, so we skip if there's truly no meeting yet.
    const { data: meeting } = await from('meetings')
      .select('id')
      .eq('project_id', projectId)
      .order('meeting_date', { ascending: false })
      .limit(1)
      .maybeSingle();
    if (!meeting) {
      // Without a meeting we can't attach an action item, so we just
      // mark the no-show on the attendance row. The widget will show
      // "Flagged" instead of "Create action item" so the GC can call
      // the foreman manually.
      const nowIso = new Date().toISOString();
      if (attendance) {
        await from('crew_attendance')
          .update({ no_show_flagged_at: nowIso } as never)
          .eq('id', attendance.id);
      } else {
        await from('crew_attendance').insert({
          project_id: projectId,
          crew_id: crewId,
          attendance_date: date,
          planned_arrival_time: crew.planned_arrival_time,
          no_show_flagged_at: nowIso,
        } as never);
      }
      return { workflow: 'crew_no_show', skipped: { reason: 'no meeting exists yet — flagged on attendance row only' } };
    }

    const description =
      `Call ${crew.name}${crew.trade ? ` (${crew.trade})` : ''} — 60+ min late on ${date}.` +
      (crew.planned_arrival_time ? ` Planned arrival: ${crew.planned_arrival_time}.` : '');

    const { data: insertedItem, error: insertErr } = await from('meeting_action_items')
      .insert({
        meeting_id: meeting.id,
        description,
        priority: 'high',
        status: 'open',
        due_date: date,
      } as unknown as Record<string, unknown>)
      .select('id')
      .single();

    if (insertErr) return { workflow: 'crew_no_show', error: insertErr.message };
    if (!insertedItem) return { workflow: 'crew_no_show', error: 'insert returned no row' };

    // Back-link onto crew_attendance — creating the row if it doesn't exist.
    const nowIso = new Date().toISOString();
    if (attendance) {
      await from('crew_attendance')
        .update({
          no_show_flagged_at: nowIso,
          meeting_action_item_id: insertedItem.id,
        } as never)
        .eq('id', attendance.id);
    } else {
      await from('crew_attendance').insert({
        project_id: projectId,
        crew_id: crewId,
        attendance_date: date,
        planned_arrival_time: crew.planned_arrival_time,
        no_show_flagged_at: nowIso,
        meeting_action_item_id: insertedItem.id,
      } as never);
    }

    return {
      workflow: 'crew_no_show',
      created: { type: 'task', id: insertedItem.id as string },
    };
  } catch (err) {
    return { workflow: 'crew_no_show', error: (err as Error).message };
  }
}
